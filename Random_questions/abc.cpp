#include<bits/stdc++.h>
using namespace std;

void solve(string &S,int n){
    for(int i=0;i<=n;i++){
        if(S[i]=='e' && S[i+1]=='r'){
            cout<<"er"<<endl;
        }
        if(S[i]=='i'&&S[i+1]=='s'&& S[i+2]=='t'){
            cout<<"ist"<<endl;
            
    }



}
}

int main(){

    string S;
    int n=S.length();
    solve(S,n);


}